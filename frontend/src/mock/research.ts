export function getResearchReports(keyword?: string): ResearchReport[] {
  let results = [...mockResearchReports]

  if (keyword) {
    const kw = keyword.toLowerCase()
    results = results.filter(
      (r) =>
        r.title.toLowerCase().includes(kw) ||
        r.institution.toLowerCase().includes(kw) ||
        r.summary.toLowerCase().includes(kw) ||
        r.bonds.some((b) => b.code.toLowerCase().includes(kw) || b.name.toLowerCase().includes(kw))
    )
  }

  return results.sort((a, b) => b.publish_date.localeCompare(a.publish_date))
}

export function getResearchReportById(id: string): ResearchReport | undefined {
  return mockResearchReports.find((r) => r.id === id)
}
